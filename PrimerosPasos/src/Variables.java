
public class Variables {

	public static void main(String[] args) {

		byte edad = 35;
		
		System.out.println("Este es un tipo de dato byte: -128 a 127; " + edad);
		
		
		short numero = 1000;
		
		System.out.println("Este es un tipo de dato short: -32768 a 32767; " + numero);
		
		int enteros = 115489156;
		
		System.out.println("Este es un tipo de dato intero: -2147483648 a 2147486649; " + enteros);
	}

}
