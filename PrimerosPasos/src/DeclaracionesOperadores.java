
public class DeclaracionesOperadores {

	public static void main(String[] args) {

		int a=5;
		int b;
		
		b=7;
		
		int c = b + a;
		int c1 = b * a;
		int c2 = b - a;
		double c3 = b / a;
		
		c++;
		
		c1 += 6;
		
		c2 -= 7;
		
		c3--;
		
		System.out.println(c);
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
		
		
	}

}
